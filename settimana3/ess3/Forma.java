package settimana3.ess3;

public class Forma {
    protected double base;
    protected double altezza;
    protected double area;


    public Forma(double base, double altezza,double area) {
        this.base = base;
        this.altezza = altezza;
        this.area = area;
    }

    public  double calcolaArea(){
        area = 0 ;
        return area ;
     } 
    
}
